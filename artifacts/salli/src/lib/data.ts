// Helper to get daily motivation
export function getDailyMotivation() {
  const dayOfYear = Math.floor(
    (Date.now() - new Date(new Date().getFullYear(), 0, 0).getTime()) / 86400000
  );
  return MOTIVATIONS[dayOfYear % MOTIVATIONS.length];
}

export function getPreviousMotivations(count = 6) {
  const dayOfYear = Math.floor(
    (Date.now() - new Date(new Date().getFullYear(), 0, 0).getTime()) / 86400000
  );
  const result = [];
  for (let i = 1; i <= count; i++) {
    const index = (dayOfYear - i + MOTIVATIONS.length * 100) % MOTIVATIONS.length;
    result.push(MOTIVATIONS[index]);
  }
  return result;
}
